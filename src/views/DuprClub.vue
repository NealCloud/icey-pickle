<script>
import { ref } from 'vue'
import {db} from '../firebase/config'
import {addDoc, collection} from 'firebase/firestore'
import getUser from '../composables/getUser'

export default {
  setup() {
    const first = ref('')
    const last = ref('')
    const email = ref('')
    const {user} = getUser()

    const handleSubmit = async () => {
      const colRef = collection(db, 'waitlistMlp')

      await addDoc(colRef, {
        first: first.value,
        last: last.value,
        email: email.value,
        isConfirmed: false,
        userId: null,
        admin: user.value.uid
      })

      first.value = ''
      last.value = '' 
      email.value = '' 
    }
    return { handleSubmit, first, last, email }
  }
}
</script>

